VulkanContext::VulkanContext(GLFWwindow* window, bool enableValidation)
	: m_bValidationLayers(enableValidation), m_window(window)
{
	createInstance();           // 创建 Vulkan 实例（连接应用程序与驱动）
	setupDebugMessenger();      // 设置调试回调（捕获 Vulkan 错误和警告）
	createSurface(window);            // 创建窗口表面（连接 Vulkan 与操作系统窗口）
	pickPhysicalDevice();       // 选择物理 GPU（如 NVIDIA/AMD 显卡）
	createLogicalDevice();      // 创建逻辑设备（定义 GPU 的功能接口）
}

void VulkanContext::ReleaseResouce()
{
	vkDestroyDevice(m_device, nullptr);                        // 销毁逻辑设备
	if (m_bValidationLayers) {
		DestroyDebugUtilsMessengerEXT(m_instance, m_debugMessenger, nullptr); // 销毁调试工具
	}
	vkDestroySurfaceKHR(m_instance, m_surface, nullptr); // 销毁窗口表面
	vkDestroyInstance(m_instance, nullptr);           // 销毁 Vulkan 实例
}

